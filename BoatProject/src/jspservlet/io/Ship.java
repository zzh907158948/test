package jspservlet.io;

public class Ship {
	private int shipID;
	private String distinType;
	private String shipName;
	private float shipWidth;
	private float tonnage;
	private float shipLength;
	private float posiLength;
	private float posiWidth;
	public int getShipID() {
		return shipID;
	}
	public void setShipID(int shipID) {
		this.shipID = shipID;
	}
	public String getDistinType() {
		return distinType;
	}
	public void setDistinType(String distinType) {
		this.distinType = distinType;
	}
	public String getShipName() {
		return shipName;
	}
	public void setShipName(String shipName) {
		this.shipName = shipName;
	}
	public float getShipWidth() {
		return shipWidth;
	}
	public void setShipWidth(float shipWidth) {
		this.shipWidth = shipWidth;
	}
	public float getTonnage() {
		return tonnage;
	}
	public void setTonnage(float tonnage) {
		this.tonnage = tonnage;
	}
	public float getShipLength() {
		return shipLength;
	}
	public void setShipLength(float shipLength) {
		this.shipLength = shipLength;
	}
	public float getPosiLength() {
		return posiLength;
	}
	public void setPosiLength(float posiLength) {
		this.posiLength = posiLength;
	}
	public float getPosiWidth() {
		return posiWidth;
	}
	public void setPosiWidth(float posiWidth) {
		this.posiWidth = posiWidth;
	}

}
